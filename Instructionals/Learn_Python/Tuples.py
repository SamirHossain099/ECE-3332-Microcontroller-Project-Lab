#Tuples and lists are similar
#Tuples can also store multiple data types
#syntax: name = (<value>)
#Tuples are immutable, they can not be changed or modified.
#The number, size, and value of the elements can not be changed once the tuple is created.
coordinates = (4,5)
#Tuples can be accessed
print(coordinates[0]) 
#But they can not be modified (will throw error)

#A list of tuples can be created
coords = [(4,5),(6,7)]
#Tuples are more nieche to use, but they are great when you want to store data that can not be changed